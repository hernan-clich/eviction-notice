const eligibleLookup = new Set(ELIGIBLE_TOKENS.map((symbol) => symbol.toLowerCase()));

/** Case-insensitive membership check against the eligible universe. */
export function isEligibleToken(symbol: string): boolean {
  return eligibleLookup.has(symbol.toLowerCase());
}

